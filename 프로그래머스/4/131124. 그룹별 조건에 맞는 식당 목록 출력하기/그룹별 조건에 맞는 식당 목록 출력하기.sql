SELECT P.MEMBER_NAME, R.REVIEW_TEXT, R.REVIEW_DATE
FROM REST_REVIEW AS R
INNER JOIN MEMBER_PROFILE AS P ON P.MEMBER_ID = R.MEMBER_ID
WHERE R.MEMBER_ID = (
    SELECT MEMBER_ID 
    FROM REST_REVIEW
    GROUP BY MEMBER_ID
    ORDER BY COUNT(MEMBER_ID) DESC LIMIT 1
    )
ORDER BY R.REVIEW_DATE ASC, R.REVIEW_TEXT ASC
